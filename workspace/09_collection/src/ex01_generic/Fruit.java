package ex01_generic;

public class Fruit {

	// field
	private String name;
	private int price;
	
	// constructor
	public Fruit(String name, int price) {
		this.name = name;
		this.price = price;
	}

	
	// method : Getter and Setter
	
	
	
	
	
	
	// method : toString() : Override
	@Override
	public String toString() {
		return "Fruit [name = "+ name +", price = "+ price +" ]";
	}
	
	
	
	
	
	
	
	
	
	
	
}
